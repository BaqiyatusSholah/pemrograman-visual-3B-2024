    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String strcash = incash.getText();
        int cash = Integer.parseInt(strcash);
        String strharga = outharga.getText();
        int harga = Integer.parseInt(strharga);
        if(cash < harga){
            JOptionPane.showMessageDialog(null, "Saldo Anda Kurang", "Transaksi Gagal",JOptionPane.ERROR_MESSAGE);
        }else{
            int kembalian = cash - harga;
            String strkembalian = Integer.toString(kembalian);
            JOptionPane.showMessageDialog(null, "Terima Kasih", "Transaksi Berhasil",JOptionPane.INFORMATION_MESSAGE);
            outcash.setText(strkembalian);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

