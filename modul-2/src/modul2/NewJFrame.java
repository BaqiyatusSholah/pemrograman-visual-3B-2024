    private void btnpesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesanActionPerformed
        String roti = "";
        String topping = "";
        String jumlah = "0";
        int total = 0;
        int bagel = 17000;
        int croissant = 21000;
        int brioche = 30000;
        int bialy = 18000;
        int challah = 25000;
        int breadstick = 25000;
        int bolillo = 35000;
        int ciabatta = 17000;
        int coklat = 5000;
        int kacang = 3000;
        int keju = 7000;
        
        if (inbagel.isSelected()){
            total = total + bagel;
            outroti.setText("Bagel");
            roti = "bagel";
        }else if (incroissant.isSelected()){
            total = total + croissant;
            outroti.setText("Croissant");
            roti = "croissant";
        }else if (inciabatta.isSelected()){
            total = total + ciabatta;
            outroti.setText("Ciabatta");
            roti = "ciabatta";
        }else if (inbrioche.isSelected()){
            total = total + brioche;
            outroti.setText("Brioche");
            roti = "brioche";
        }else if (inbialy.isSelected()){
            total = total + bialy;
            outroti.setText("Bialy");
            roti = "bialy";
        }else if (inchallah.isSelected()){
            total = total + challah;
            outroti.setText("Challah");
            roti = "challah";
        }else if (inbreadstick.isSelected()){
            total = total + breadstick;
            outroti.setText("Breadstick");
            roti = "breadstick";
        }else if (inbolillo.isSelected()){
            total = total + bolillo;
            outroti.setText("Bolillo");
            roti = "bolillo";
        }
        
        if (ingatopping.isSelected()){
            total = total + 0;
            outtopping.setText("No Topping");
            topping = "notopping";
        }else if (incoklat.isSelected()){
            total = total + coklat;
            outtopping.setText("Coklat");
            topping = "coklat";
        }else if (inkacang.isSelected()){
            total = total + kacang;
            outtopping.setText("Kacang");
            topping = "kacang";
        }else if (inkeju.isSelected()){
            total = total + keju;
            outtopping.setText("Keju");
            topping = "keju";
        }
        
        if("".equals(roti) || "".equals(topping) || "0".equals(injumlah.getText())){
            JOptionPane.showMessageDialog(null, "Isi pesanan dengan benar", "Pesanan Gagal",JOptionPane.WARNING_MESSAGE);
        }else{
            jumlah = injumlah.getText();
            Integer intjumlah = Integer.parseInt(jumlah);
            outjumlah.setText(jumlah);
            total = total * intjumlah;
        
            if(total >= 100000){
             total = total - (total * 5 / 100);
                outdiskon.setText("5%");
            }else{
                outdiskon.setText("0%");
            }  
        
            String strtotal = Integer.toString(total);
            outharga.setText(strtotal);
        
            panelpesan.setVisible(false);
            panelcheckout.setVisible(true);
        }
    }//GEN-LAST:event_btnpesanActionPerformed

